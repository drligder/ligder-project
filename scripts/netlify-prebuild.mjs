/**
 * Runs before `vite build`. Writes `public/_redirects` for Netlify:
 * - Proxies /api/* → Railway (same-origin /api in the browser, no CORS issues).
 * - SPA fallback for client routes.
 *
 * Uses process.env.VITE_API_BASE only (what Netlify injects into `npm run build`).
 * Vite’s own env handling is not involved here.
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, '..');
const outPath = path.join(root, 'public', '_redirects');

const raw = process.env.VITE_API_BASE?.trim() ?? '';
let base = raw.replace(/\/$/, '');
if (base.endsWith('/api')) base = base.slice(0, -4);

const lines = [
  '# Generated by scripts/netlify-prebuild.mjs — do not edit by hand',
];

if (base && /^https?:\/\//i.test(base)) {
  lines.push(`/api/* ${base}/api/:splat 200`);
  lines.push('/* /index.html 200');
  lines.push('');
  fs.writeFileSync(outPath, lines.join('\n'), 'utf8');
  console.log(`[netlify-prebuild] Wrote public/_redirects (API proxy → ${base})`);
} else {
  lines.push(
    '# VITE_API_BASE was unset: /api/* is NOT proxied — forum will break until you set VITE_API_BASE on Netlify (Build env) and redeploy.'
  );
  lines.push('/* /index.html 200');
  lines.push('');
  fs.writeFileSync(outPath, lines.join('\n'), 'utf8');
  console.warn(
    '[netlify-prebuild] VITE_API_BASE is unset. Set it to your Railway origin (e.g. https://xxx.up.railway.app) in Netlify → Environment variables (Builds / Production) and redeploy.'
  );
}
